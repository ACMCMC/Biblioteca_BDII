    private void BuscarPrestamos(){
        ModeloTablaPrestamos mtp;

        mtp=(ModeloTablaPrestamos) tblPres.getModel();
        java.util.List<Usuario> usrs = fa.obtenerUsuarios(textIdArriba.getText(), textNombreArriba.getText());
        mtp.setFilas(usrs, fa.obtenerPrestamosVencidos(usrs));
        if (mtp.getRowCount() > 0) {
            tblPres.setRowSelectionInterval(0, 0);
            btnPrestar.setEnabled(true);
            
            int lineaseleccion=tblPres.getSelectedRow();
            Usuario usuario=mtp.obtenerUsuario(lineaseleccion);
            
        }
        else btnPrestar.setEnabled(false);
 
    }
    
